/*
	두 수의 합을 계산하는 애플리케이션
*/

public class Add {
    public static void main(String[] args) {
         int x;
         int y;
         int sum;
  
         x = 100;
         y = 200;
         sum = x + y;

         System.out.println(sum);
         return; // method(main)를 종료하고 돌아간다는 의미 
    }
}